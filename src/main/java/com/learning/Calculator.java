package com.learning;

public class Calculator {
    public long add( long num1, long num2 ) {
        return num1 + num2;
    }

    public long sub( long num1, long num2 ) {
        return num1 - num2;
    }

    public long multiply( long num1, long num2 ) {
        return num1 - num2;
    }

    public long division( long num1, long num2 ) {
        return num1 / num2;
    }
}
